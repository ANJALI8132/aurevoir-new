                            <p>Neatly tucked into a hillock, bang amidst a wildlife sanctuary and nature reserve along the Western Ghats or the Sahyadiri Mountain (mountain of patience) ranges at the Palakkad Pass in Kerala and bordering the neighboring state of Tamil Nadu, the Au Revoir Wellness Retreat is a well-hidden getaway from the touristy trails of God's Own Country, yet easily accessible. With misty and often cloud-shrouded evergreen mountains in the foreground standing sentinel over a majestic lake with high-rising reserve forests in the background, the setting here is picture-perfect!</p>
                            <p>Situated about 15 kilometers from Palakkad city off the ring-road along the Malampuzha Dam (the largest reservoir in Kerala), the drive to Au Revoir is breathtakingly beautiful and getting there is half the fun. Situated a few kilometers away from the often crowded environs of the dam with its amusement parks and gardens, the narrow but well-maintained road snakes alongside the crystal clear waters of the catchment area on the one side and rubber plantations and the reserve forest on the other. En route, there are a number of scenic spots perfect for wetting your feet, picnics, angling and taking picturesque photographs and selfies.</p>
