     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $bill = new bill([
            'bill_user' => $request->get('user'),
            'bill_from' => $request->get('date_from'),
            'bill_to' => $request->get('date_to'),
            'bill_invoice' => $request->get('invoiceno'),
            'bill_amount' => $request->get('amount'),
            'bill_tax' => $request->get('tax')
        ]);

        $bill->save();

        return view('bill');
    }

